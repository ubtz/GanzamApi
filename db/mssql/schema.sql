CREATE TABLE StoreProducts (
    Id BIGINT IDENTITY(1,1) PRIMARY KEY,
    StoreId BIGINT NOT NULL,
    ProductId BIGINT NOT NULL,
    Price DECIMAL(18,2) NOT NULL,
    StockQty DECIMAL(18,3) NOT NULL DEFAULT 0,
