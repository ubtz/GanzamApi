GO

CREATE TABLE Categories (
    Id BIGINT IDENTITY(1,1) PRIMARY KEY,
    ParentId BIGINT NULL,
    Name NVARCHAR(120) NOT NULL,
    ImageUrl NVARCHAR(500) NULL,
